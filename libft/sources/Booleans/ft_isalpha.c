#include "../../includes/libft.h"

/**
 * @brief Checks for an alphabetic character;
 * 
 * @param c The character to be checked;
 * @return true if the character is an alphabetic character; otherwise, false;
 */
bool	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (true);
	return (false);
}
